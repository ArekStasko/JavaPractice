public class WhileLoop {
    public static void Run(){
        int s = 0;
        int i = 20;
        do{
            s++;
            i++;
        }
        while(i <= 10);
        System.out.println(s);

        int x = 0;
        int y = 20;
        while(y <= 10){
            x++;
            y++;
        };
        System.out.println(x);

    }
}
